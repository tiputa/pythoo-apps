import random


def omikuji(seed_number):
    fortunes = [
        "大吉 🐉 今日は最高の運勢！",
        "中吉 🌈 チャンスはしっかり掴んで！",
        "小吉 ☕ 少しずつ前進しよう",
        "吉 ✨ 普通が一番幸せかも",
        "末吉 💫 焦らず、地道にね",
        "凶 😖 注意深く行動しよう",
        "大凶 💥 今日は静かに過ごすと吉",
    ]

    colors = ["赤", "青", "緑", "黄色", "紫", "白", "黒", "オレンジ", "ピンク", "水色"]

    advices = [
        "いつもより5分早く行動してみよう。",
        "小さなことにも「ありがとう」を言ってみて。",
        "目を閉じて深呼吸、気持ちが整うよ。",
        "失敗しても、それは次のチャンスの準備だよ。",
        "今日は「やってみよう」がキーワード！",
        "他人と比べず、自分のペースでOK。",
        "迷ったら、好きなほうを選んで正解。",
    ]

    # シード値を使ってランダムだけど“固定っぽい”結果を出す
    random.seed(seed_number)

    result = random.choice(fortunes)
    lucky_number = random.randint(1, 9)
    lucky_color = random.choice(colors)
    advice = random.choice(advices)

    print("🔮 あなたの選んだ数字から導かれた運勢は…")
    print(result)
    print(f"🎯 ラッキーナンバー：{lucky_number}")
    print(f"🎨 ラッキーカラー：{lucky_color}")
    print(f"💬 アドバイス：{advice}")


# 数字を入力してもらう
try:
    user_input = int(input("🔢 好きな数字（1〜99）を入力してください："))
    if 1 <= user_input <= 99:
        omikuji(user_input)
    else:
        print("⚠️ 1〜99の数字を入力してね。")
except ValueError:
    print("⚠️ 数字をちゃんと入力してね。")


# ここまでがコードの全体です。おみくじの結果を表示するプログラムです。
# ユーザーが入力した数字をシード値として使い、ランダムな運勢やラッキーナンバー、カラー、アドバイスを表示します。
# 数字は1〜99の範囲で入力を求め、
# それ以外の入力や非数値の入力に対してはエラーメッセージを表示します。
# これにより、ユーザーは自分の選んだ数字に基づいて、特別な運勢を体験できます。
# もし数字が範囲外だったり、非数値が入力された場合、
# 適切なエラーメッセージが表示されるようになっています。
